package replitquestions.loops;

public class Q14_Alphabets {
    public static void main(String[] args) {

        /*
        Print Alphabet on the console.

OUTPUT  : a b c .. .. .. .. y z
         */
        char alphabets = 'a';
        while (alphabets <= 'z') {
            System.out.print(alphabets + " ");
            alphabets++;
        }
    }


}
